-- Add profile_id column to bookings
ALTER TABLE public.bookings ADD COLUMN profile_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE;

-- Migrate existing bookings to use profile_id
UPDATE public.bookings 
SET profile_id = (
  SELECT p.id 
  FROM public.profiles p 
  WHERE p.user_id = bookings.user_id
